public class MainCondicionales {

    public static void main(String[] args) {
        int numeroIf = 5;

        if (numeroIf == 0) {
            System.out.println("es 0");
        } else if (numeroIf > 0) {
            System.out.println("es positivo");
        } else {
            System.out.println("es negativo");
        }
    }
}